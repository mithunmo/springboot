package com.f9g4.web.forms;


public class BroadcastMessageForm {

	private String templateCode;
	private String messageSubject;
	private String messageBody;
	private Integer messageScope;
	
	
	public String getMessageSubject() {
		return messageSubject;
	}
	public void setMessageSubject(String messageSubject) {
		this.messageSubject = messageSubject;
	}
	public String getTemplateCode() {
		return templateCode;
	}
	public void setTemplateCode(String templateCode) {
		this.templateCode = templateCode;
	}
	public String getMessageBody() {
		return messageBody;
	}
	public void setMessageBody(String messageBody) {
		this.messageBody = messageBody;
	}
	public Integer getMessageScope() {
		return messageScope;
	}
	public void setMessageScope(Integer messageScope) {
		this.messageScope = messageScope;
	}
	
	@Override
	public String toString() {
		return "BroadcastMessageForm [templateCode=" + templateCode
				+ ", messageSubject=" + messageSubject + ", messageBody="
				+ messageBody + ", messageScope=" + messageScope + "]";
	}
}
