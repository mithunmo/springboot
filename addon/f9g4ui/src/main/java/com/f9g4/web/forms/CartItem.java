package com.f9g4.web.forms;

import java.math.BigDecimal;

import javax.xml.datatype.XMLGregorianCalendar;

public class CartItem {
	
	private Integer boardId;
	private BigDecimal boardprice;
	private BigDecimal percentage;
	private String boardName;
	private Integer fileType;
	private Integer designerId;
	private String designerName;
	private String imageUrl;
	private String tileImageUrl;
	private String designerLogo;
	private boolean hasAltered;
	private XMLGregorianCalendar lastUpdateDate;
	
	
	public String getTileImageUrl() {
		return tileImageUrl;
	}
	public void setTileImageUrl(String tileImageUrl) {
		this.tileImageUrl = tileImageUrl;
	}
	public boolean isHasAltered() {
		return hasAltered;
	}
	public void setHasAltered(boolean hasAltered) {
		this.hasAltered = hasAltered;
	}
	public XMLGregorianCalendar getLastUpdateDate() {
		return lastUpdateDate;
	}
	public void setLastUpdateDate(XMLGregorianCalendar lastUpdateDate) {
		this.lastUpdateDate = lastUpdateDate;
	}
	public Integer getFileType() {
		return fileType;
	}
	public void setFileType(Integer fileType) {
		this.fileType = fileType;
	}
	public Integer getDesignerId() {
		return designerId;
	}
	public void setDesignerId(Integer designerId) {
		this.designerId = designerId;
	}
	public String getDesignerLogo() {
		return designerLogo;
	}
	public void setDesignerLogo(String designerLogo) {
		this.designerLogo = designerLogo;
	}
	public String getImageUrl() {
		return imageUrl;
	}
	public void setImageUrl(String imageUrl) {
		this.imageUrl = imageUrl;
	}
	public String getBoardName() {
		return boardName;
	}
	public void setBoardName(String boardName) {
		this.boardName = boardName;
	}
	public String getDesignerName() {
		return designerName;
	}
	public void setDesignerName(String designerName) {
		this.designerName = designerName;
	}
	public Integer getBoardId() {
		return boardId;
	}
	public void setBoardId(Integer boardId) {
		this.boardId = boardId;
	}
	public BigDecimal getBoardprice() {
		return boardprice;
	}
	public void setBoardprice(BigDecimal boardprice) {
		this.boardprice = boardprice;
	}
	public BigDecimal getPercentage() {
		return percentage;
	}
	public void setPercentage(BigDecimal percentage) {
		this.percentage = percentage;
	}
	
	@Override
	public String toString() {
		return "CartItem [boardId=" + boardId + ", boardprice=" + boardprice
				+ ", percentage=" + percentage + ", boardName=" + boardName
				+ ", fileType=" + fileType + ", designerId=" + designerId
				+ ", designerName=" + designerName + ", imageUrl=" + imageUrl
				+ ", tileImageUrl=" + tileImageUrl + ", designerLogo="
				+ designerLogo + ", hasAltered=" + hasAltered
				+ ", lastUpdateDate=" + lastUpdateDate + "]";
	}
}
